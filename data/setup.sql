DROP TABLE IF EXISTS children CASCADE;
DROP TABLE IF EXISTS toys;

CREATE TABLE children (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  age INT NOT NULL
);

CREATE TABLE toys (
  id BIGINT GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  type TEXT NOT NULL,
  name TEXT NOT NULL, 
  color TEXT,
  child_id BIGINT NOT NULL REFERENCES children(id)
  
)